function renameBlock(block, pool) {
  const map = new Map();
  let poolIdx = 0;
  return block.replace(/\blocal(\d+)\b/g, (full, num) => {
    if (!map.has(full)) {
      const name =
        poolIdx < pool.length ? pool[poolIdx++] : `scopedVar${num}`;
      map.set(full, name);
    }
    return map.get(full);
  });
}

function processFile(file) {
  const filePath = path.join(JS_DIR, file);
  let code = fs.readFileSync(filePath, "utf8");
  const pool = FILES[file];
  if (!pool) {
    return;
  }
  for (const [from, to] of GLOBAL_PREFIX[file] || []) {
    code = code.replace(from, to);
  }
  const chunks = code.split(/(?=^\s{6}[a-zA-Z_$][\w$]*\s*(?:\([^)]*\))?\s*\{)/m);
  const out = chunks.map((chunk, i) => {
    if (i === 0) {
      return renameBlock(chunk, pool);
    }
    return renameBlock(chunk, pool);
  });
  code = out.join("");
  code = renameBlock(code, pool);
  fs.writeFileSync(filePath, code);
  const remaining = (code.match(/\blocal\d+\b/g) || []).length;
  console.log(file, "remaining localN:", remaining);
}

for (const file of Object.keys(FILES)) {
  processFile(file);
}

